    private void jButtonCargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCargarActionPerformed
        JFileChooser examinar = new JFileChooser();
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("PNG", "png");
        examinar.setFileFilter(filtro);
        if (examinar.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            File archivo = examinar.getSelectedFile();
            if (archivo.getName().endsWith("png")) {
                Toolkit tk = Toolkit.getDefaultToolkit();
                rutaImagen = examinar.getSelectedFile().toString();
                Image image = tk.createImage(rutaImagen);
                jLabelRuta.setIcon(new ImageIcon(image.getScaledInstance(jLabelRuta.getWidth(), jLabelRuta.getHeight(), Image.SCALE_AREA_AVERAGING)));
            } else {
                JOptionPane.showMessageDialog(null, "Por favor ingrese una imagen PNG");
            }
        }
    }//GEN-LAST:event_jButtonCargarActionPerformed

    private void jButtonAnalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAnalizarActionPerformed
        if (jTextFieldNumeros.getText().compareToIgnoreCase("") != 0) {
            try {
                InputStream imagen = new FileInputStream(rutaImagen);
                AreaImagen AI = new AreaImagen(imagen, Integer.parseInt(jTextFieldNumeros.getText()));
                AI.Control();
                String[][] auxiliar = AI.getPuntosAleatorios();
                jLabelAnchoImagen.setText(Integer.toString(auxiliar[0].length));
                jLabelAltoImagen.setText(Integer.toString(auxiliar.length));
                jLabelAreaTotal.setText(Integer.toString(AI.getAreaTotal()));
                jLabelTotalAleatorios.setText(Integer.toString(AI.getCantidadPuntos()));
                jLabelPuntosBlancos.setText(Integer.toString(AI.getPuntosBlanco()));
                jLabelPuntosNegro.setText(Integer.toString(AI.getPuntosNegro()));
                jLabelAreaNegro.setText(Double.toString(AI.getAreaEnNegro()));
            } catch (FileNotFoundException ex) {
                Logger.getLogger(AreaImagenVista.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Ingrese la cantidad de puntos que desea generar");
        }
    }//GEN-LAST:event_jButtonAnalizarActionPerformed

